export const t = (key: string, language: Language): string => {
  if (language === 'tr') {
    return translationsTr[key] ?? key;
  }
  return key;
};

export const getLocale = (language: Language): string => (language === 'tr' ? 'tr-TR' : 'en-US');

export const formatDateDisplay = (
  date: Date,
  language: Language,
  options: Intl.DateTimeFormatOptions = { year: 'numeric', month: 'short', day: 'numeric' }
): string => {
  return new Intl.DateTimeFormat(getLocale(language), options).format(date);
};

export const getMonthNames = (language: Language, formatStyle: 'long' | 'short' = 'long'): string[] => {
  const formatter = new Intl.DateTimeFormat(getLocale(language), { month: formatStyle });
  return Array.from({ length: 12 }, (_, i) => formatter.format(new Date(2020, i, 1)));
};

export const getWeekdayNames = (language: Language, formatStyle: 'short' | 'long' = 'short'): string[] => {
  const formatter = new Intl.DateTimeFormat(getLocale(language), { weekday: formatStyle });
  const start = new Date(2020, 10, 1); // Sunday
  return Array.from({ length: 7 }, (_, i) => formatter.format(new Date(2020, 10, 1 + i)));
};
