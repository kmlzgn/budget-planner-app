export const tKey = (key: TranslationKey, language: Language): string => {
  if (language === 'tr') {
    const value = translationsTrTyped[key];
    if (!value) {
      if (import.meta.env?.DEV) {
        throw new Error(`Missing TR translation for key: ${key}`);
      }
      return translationsEn[key] ?? key;
    }
    return value;
  }
  return translationsEn[key] ?? key;
};

